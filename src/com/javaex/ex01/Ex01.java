package com.javaex.ex01;

// 클래스
public class Ex01 {

	// 매소드
	public static void main(String[] args) {

		int myAge; // 1.변수선언
		myAge = 25; // 2.초기화 (처음값)
		System.out.println(myAge); // 3.출력 매소드
		myAge = 100; // 4.대입
		System.out.println(myAge); // 5.출력 매소드
		int youAge = 30;
		System.out.println(youAge);
	}
}